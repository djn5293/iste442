public class Leaked {
    private String name;
    private String email;
    private String city;
    private String mac;
    private String timestamp;
    private String creditcard;
    public Leaked(String _name, String _email, String _city, String _mac, String _timestamp, String _creditcard){
        this.name = _name;
        this.email = _email;
        this.city = _city;
        this.mac = _mac;
        this.timestamp = _timestamp;
        this.creditcard = _creditcard;
    }
}
